	if ngay==3:
		print('Hôm nay là thứ 4')
	if ngay==4:
		print('Hôm nay là thứ 5')
	if ngay==5:
		print('Hôm nay là thứ 6')
	if ngay==6:
		print('Hôm nay là thứ 7')
